      寒武纪的创业团队源自中国科学院计算技术研究所智能处理器研究中心，拥有深厚的学术积累，团队作为第一单位，与Inria等机构合作发表的DianNao系列论文，于2014-2016年期间多次获得计算机体系结构各大顶级学术会议最佳论文奖/最高分论文。寒武纪团队成员前期在智能处理器指令集和架构方面的基础研究成果在业界获得了崇高声誉，相关研究成果得到国内外学界广泛关注，极大地推动了全球智能处理器芯片的研发。

  寒武纪在中国上海发布了首款云端智能芯片MLU100
